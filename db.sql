CREATE TABLE IF NOT EXISTS RADA (
  ID SERIAL,
  X DECIMAL(12,10) NOT NULL,
  Y DECIMAL(12,10) NOT NULL,
  Nimi VARCHAR(300) NOT NULL,
  Kirjeldus VARCHAR(300) NULL,
  PRIMARY KEY (ID));
  
