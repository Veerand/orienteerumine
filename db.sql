  ID SERIAL,
  X DECIMAL(12,10) NOT NULL,
  Y DECIMAL(12,10) NOT NULL,
  kysimus VARCHAR(300) NOT NULL,
  vastus1 VARCHAR(300) NOT NULL,
  vastus2 VARCHAR(300) NOT NULL,
  vastus3 VARCHAR(300) NOT NULL,
  vastus4 VARCHAR(300) NOT NULL,
  Nimi VARCHAR(45) NOT NULL,
  Rada INT NOT NULL,  
